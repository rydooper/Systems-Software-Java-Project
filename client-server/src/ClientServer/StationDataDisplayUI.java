    private void stationDropBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_stationDropBtnActionPerformed
		Random RandGen = new  Random();
		Integer index = stationDropBtn.getSelectedIndex(); 
		index++;
		if(!(index == 0)){
			mainMenuUI.dataRequest = "WEATHER STATION,"+index;		
			mainMenuUI.buttonPressed = true;
			try{
				Thread.sleep(400);
			}
			catch(InterruptedException e){
				System.out.println(e.getMessage());
			}
			mainMenuUI.dataRequest = "";
			
			Integer fieldNum = (index-1) / 3;
			fieldNum++;
			
			double dLat = 51.942, dLon = -3.452;
			double lat = (RandGen.nextInt(100) + (fieldNum*100));
			double newLat = lat/10000;
			lat = dLat + newLat;
			double lon = (RandGen.nextInt(100) + (fieldNum*100));
			double newLon = lon/10000;
			lon = dLon + newLon;
                        DecimalFormat Rounded = new DecimalFormat("##.00000"); //method of rounding the coordinates was adapted from Stack Overflow (https://stackoverflow.com/questions/11701399/round-up-to-2-decimal-places-in-java)
                        String latRounded = Rounded.format(lat);
                        String lonRounded = Rounded.format(lon);
			/*latBox.setText(valueOf(lat));
			lonBox.setText(valueOf(lon));*/
                        latBox.setText(latRounded);
                        lonBox.setText(lonRounded);

			
			fieldNumBox.setText(fieldNum.toString());
			stationNoInput.setText(index.toString());
			humidityInput.setText(WSData.get(index)[0]);
			windInput.setText(WSData.get(index)[1]);
			precipitationInput.setText(WSData.get(index)[2]);
		}
		
		
    }//GEN-LAST:event_stationDropBtnActionPerformed

